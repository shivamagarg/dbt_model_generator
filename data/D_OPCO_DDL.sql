CREATE TABLE IF NOT EXISTS DW.D_OPCO
   (
    OPCO_ID NUMBER NOT NULL,
	DATA_SRC VARCHAR(200 CHAR) NOT NULL,
	CREATE_DT TIMESTAMP_NTZ(9) NOT NULL DEFAULT CAST(CONVERT_TIMEZONE('UTC', CAST(CURRENT_TIMESTAMP() AS TIMESTAMP_TZ(9))) AS TIMESTAMP_NTZ(9)),
	CREATE_BY VARCHAR(200 CHAR) NOT NULL,
	CREATE_PGM VARCHAR(200 CHAR) NOT NULL,
	UPDATE_DT TIMESTAMP_NTZ(9) NOT NULL DEFAULT CAST(CONVERT_TIMEZONE('UTC', CAST(CURRENT_TIMESTAMP() AS TIMESTAMP_TZ(9))) AS TIMESTAMP_NTZ(9)),
	UPDATE_BY VARCHAR(200 CHAR) NOT NULL,
	UPDATE_PGM VARCHAR(200 CHAR) NOT NULL,
	OPCO_CD VARCHAR(30 CHAR) NOT NULL,
	OPCO_DSC VARCHAR(200 CHAR),
	RPT_OPCO_DSC VARCHAR(200 CHAR),
	RPT_OPCO_ABBRV VARCHAR(10 CHAR),
    TBA_ACTIVE_FLG	VARCHAR(1),
	PRIMARY KEY (OPCO_ID),
	UNIQUE (OPCO_CD)
   );